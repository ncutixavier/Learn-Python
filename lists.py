friends = ["fab", "tuyg", "james", "jane", "claire", "muhire", "simoni"]
print(friends)  # print all items of list
print(friends[-2])  # print item on index [-2]
print(friends[:2])  # print only the first two items
print(friends[3:])  # print all items except the first three
print(friends[1:3])  # print the item2 and item3

friends[1] = "aimuhire"  # this will replace the item with index[1]
print(friends)
print("IN LIST, YOU CAN ALSO insert NUMBERS, BOOLEAN, STRINGS")

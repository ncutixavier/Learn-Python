# Overloading: act oof uusing the same function name with different parameter
class Chef:
    def make_fresh_dish(self):
        print("chef make best fresh dish")

    def make_fresh_rice (self):
        print("chef make best special rice")

    def make_salad(self):
        print("chef make salad")

    def make_chicken(self):
        print("chef make chicken")